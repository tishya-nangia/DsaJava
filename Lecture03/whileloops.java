package Lecture03;

public class whileloops {
    public static void main(String[] args) {
        int n=10;
        int i=1;
        while (i<=n){
            System.out.println(i);
            i++;
        }

        //do while loop
        int j=10;
        do{
            System.out.println(j);
        }
        while(j>=0);
        j--;
    }
}
